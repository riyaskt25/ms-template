);

CREATE TABLE IF NOT EXISTS COMPANY_SALESMAN (
    COMPANY_SALESMAN_ID BIGINT AUTO_INCREMENT PRIMARY KEY,

    COMPANY_ID BIGINT NOT NULL,
    SALESMAN_ID BIGINT NOT NULL,

    CURRENT_TARGET DECIMAL(15,2),
    ACHIEVED_TARGET DECIMAL(15,2),

    ASSOCIATION_STATUS VARCHAR(20),
    JOINED_AT TIMESTAMP,
    EXITED_AT TIMESTAMP,

    CONSTRAINT FK_CS_COMPANY FOREIGN KEY (COMPANY_ID)
        REFERENCES COMPANY(COMPANY_ID),

    CONSTRAINT FK_CS_SALESMAN FOREIGN KEY (SALESMAN_ID)
        REFERENCES SALESMAN(SALESMAN_ID),

    CONSTRAINT UK_COMPANY_SALESMAN UNIQUE (COMPANY_ID, SALESMAN_ID)
);

