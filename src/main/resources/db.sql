CREATE TABLE IF NOT EXISTS USERS (
    USER_ID BIGINT AUTO_INCREMENT PRIMARY KEY,

    EMAIL_ADDRESS VARCHAR(150) NOT NULL,
    MOBILE_NUMBER VARCHAR(20) NOT NULL,

    USER_TYPE VARCHAR(20),
    FAILED_ATTEMPTS INT DEFAULT 0,
    ACCOUNT_LOCKED_FLAG CHAR(1) DEFAULT 'N',
    LAST_LOGIN_AT TIMESTAMP,
    ACCOUNT_STATUS VARCHAR(20),

    CREATED_BY BIGINT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY BIGINT,
    UPDATED_AT TIMESTAMP,
    DELETED_FLAG CHAR(1) DEFAULT 'N',
    DELETED_AT TIMESTAMP,
    VERSION_NUMBER BIGINT DEFAULT 0,

    CONSTRAINT UK_USERS_EMAIL UNIQUE (EMAIL_ADDRESS),
    CONSTRAINT UK_USERS_MOBILE UNIQUE (MOBILE_NUMBER),

    CONSTRAINT CHK_USER_LOCKED CHECK (ACCOUNT_LOCKED_FLAG IN ('Y','N')),
    CONSTRAINT CHK_USER_DELETED CHECK (DELETED_FLAG IN ('Y','N'))
);

CREATE TABLE IF NOT EXISTS COMPANY (
    COMPANY_ID BIGINT AUTO_INCREMENT PRIMARY KEY,

    USER_ID BIGINT NOT NULL,
    REGISTRATION_NUMBER VARCHAR(100) NOT NULL,
    COMPANY_STATUS VARCHAR(20),
    COMPANY_TYPE VARCHAR(50),

    CREATED_BY BIGINT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY BIGINT,
    UPDATED_AT TIMESTAMP,
    DELETED_FLAG CHAR(1) DEFAULT 'N',
    DELETED_AT TIMESTAMP,
    VERSION_NUMBER BIGINT DEFAULT 0,

    CONSTRAINT UK_COMPANY_USER UNIQUE (USER_ID),
    CONSTRAINT UK_COMPANY_REG UNIQUE (REGISTRATION_NUMBER),

    CONSTRAINT FK_COMPANY_USER FOREIGN KEY (USER_ID)
        REFERENCES USERS(USER_ID),

    CONSTRAINT CHK_COMPANY_DELETED CHECK (DELETED_FLAG IN ('Y','N'))
);

CREATE TABLE IF NOT EXISTS SALESMAN (
